# –£ –≤–∞—Å –µ—Å—Ç—å —Å–ª–æ–≤–∞—Ä—å, –≥–¥–µ –∫–ª—é—á ‚Äì –Ω–∞–∑–≤–∞–Ω–∏–µ –ø—Ä–æ–¥—É–∫—Ç–∞. –ó–Ω–∞—á–µ–Ω–∏–µ ‚Äì —Å–ø–∏—Å–æ–∫, –∫–æ—Ç–æ—Ä—ã–π —Å–æ–¥–µ—Ä–∂–∏—Ç —Ü–µ–Ω—É –∏ –∫–æ–ª-–≤–æ —Ç–æ–≤–∞—Ä–∞.
# –í—ã–≤–µ–¥–∏—Ç–µ —á–µ—Ä–µ–∑ ‚Äò‚Äô‚Äì‚Äô‚Äô –Ω–∞–∑–≤–∞–Ω–∏–µ ‚Äì —Ü–µ–Ω—É ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ.
# –° –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã –≤–≤–æ–¥–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–∞ –∏ –µ–≥–æ –∫–æ–ª-–≤–æ. n ‚Äì –≤—ã—Ö–æ–¥ –∏–∑ –ø—Ä–æ–≥—Ä–∞–º–º—ã.
# –ü–æ—Å—á–∏—Ç–∞—Ç—å —Ü–µ–Ω—É –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö —Ç–æ–≤–∞—Ä–æ–≤ –∏ —Å–∫–æ–ª—å–∫–æ —Ç–æ–≤–∞—Ä–æ–≤ –æ—Å—Ç–∞–ª–æ—Å—å –≤ –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ–º —Å–ø–∏—Å–∫–µ.


slov1 = {}  # –ø—É—Å—Ç–æ–π —Å–ª–æ–≤–∞—Ä—å, –≤ –Ω–µ–≥–æ –¥–æ–±–∞–≤–∏–º –≤—ã–±—Ä–∞–Ω–Ω—ã–µ –ø–æ–∑–∏—Ü–∏–∏–∏ –∏ –∏—Ö –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –ø–æ–∫—É–ø–æ–∫
# –∏–º–µ–µ–º —Å–ª–æ–≤–∞—Ä—å
slov = {'–∫–æ—Ç': {'cena': 50, 'kolich': 3},
        '—Ä—É—á–∫–∞': {'cena': 3, 'kolich': 200},
        '–±—ã–∫': {'cena': 500, 'kolich': 5},
        '–±–∞–Ω–∞–Ω': {'cena': 1, 'kolich': 101},
        '–±–∞—Ç–æ–Ω': {'cena': 2, 'kolich': 15},
        '–±–µ–Ω–∑–∏–Ω': {'cena': 2, 'kolich': 70}}
print(slov, '\n', '~' * 50, '\n____–≤ –Ω–∞–ª–∏—á–∏–∏_____')  # –ø—Ä–æ–≤–µ—Ä–∫–∞
# –≤—ã–≤–æ–¥–∏–º "–Ω–∞–∑–≤–∞–Ω–∏–µ ‚Äì —Ü–µ–Ω–∞ ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ" –∫—Ä–∞—Å–∏–≤–æ –≤ —Å—Ç–æ–ª–±–∏–∫, –¥–ª—è —ç—Ç–æ–≥–æ –Ω—É–∂–Ω–æ –ø–µ—Ä–µ–±—Ä–∞—Ç—å —Å–ª–æ–≤–∞—Ä—å —á–µ—Ä–µ–∑ —Ü–∏–∫–ª for
for key in slov:
    print('–Ω–∞–∏–º–µ–Ω:', key, '-', '$', slov[key]['cena'], '-', '—à—Ç:', slov[key]['kolich'])

# —á–µ—Ä–µ–∑ items()
# for key, values in slov.items():
