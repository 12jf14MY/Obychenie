kor = 0  # —Å—á–µ—Ç—á–∏–∫ –∫–æ—Ä–∑–∏–Ω—ã $ –¥–ª—è –∏—Ç–æ–≥–æ–≤–æ–π —Å—Ç–æ–∏–º–æ—Å—Ç–∏

# —á–µ—Ä–µ–∑ –±–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª while —Å–æ–∑–¥–∞–µ–º –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤–≤–æ–¥ –Ω–∞–∑–≤–∞–Ω–∏—è –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞, –≤—ã—Ö–æ–¥ –∏–∑ —Ü–∏–∫–ª–∞ n

while True:

    print('~' * 50, "\n---–í–í–û–î –ü–†–û–î–£–ö–¢–ê---")
    a = input('–≤–≤–µ–¥–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –∏–ª–∏ n-–≤—ã—Ö–æ–¥: ')
    a = a.lower()  # –æ–ø—É—Å–∫–∞–µ–º –¥–æ –Ω–∏–∂–Ω–µ–≥–æ —Ä–µ–≥–∏—Å—Ç—Ä–∞
    # –ø—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –≤–≤–µ–¥–µ–Ω–æ–≥–æ –∫–ª—é—á–∞ –≤ —Å–ª–æ–≤–∞—Ä–µ
    if a in slov:
        a1 = slov[a]['kolich']
        print('–¥–æ—Å—Ç—É–ø–Ω–æ:', a1, '–ø–æ —Ü–µ–Ω–µ:', slov[a]['cena'])
        b = int(input('–≤–≤–µ–¥–∏—Ç–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ: '))
        p = a1 - b  # –æ—Å—Ç–∞—Ç–æ–∫ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ
        if b <= a1:

            slov[a]['kolich'] = p  # –ø–µ—Ä–µ–∑–∞–ø–∏—Å—ã–≤–∞–µ–º –Ω–æ–≤–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ

            h = b * slov[a]['cena']  # —Å—Ç–æ–∏–º–æ—Å—Ç—å –Ω–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ
            print('‚àª‚àª‚àª‚àª‚àª –ø—Ä–∏–æ–±—Ä–µ—Ç–µ–Ω–æ', b, '—à—Ç', a, '–Ω–∞ —Å—É–º–º—É:', h, '‚àª‚àª‚àª‚àª‚àª\n', '   ‚àª‚àª‚àª‚àª‚àª –æ—Å—Ç–∞–ª–æ—Å—å', p, '—à—Ç', a, '‚àª‚àª‚àª‚àª‚àª')
            kor = kor + h  # –Ω–∞–∫–æ–ø–ª–µ–Ω–∏–µ –∫–æ—Ä–∑–∏–Ω—ã –ø–æ —Ü–µ–Ω–µ
            print('üëâ –û–ë–©–ê–Ø –°–£–ú–ú–ê $', kor, 'üëà')
            # –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –ø—Ä–µ–æ–±—Ä–µ—Ç–µ–Ω–Ω—ã–π —Ç–æ–≤–∞—Ä –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤ –Ω–æ–≤—ã–π —Å–ª–æ–≤–∞—Ä—å.
            if a in slov1:  # –ø—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞ –Ω–∞–ª–∏—á–∏–µ —Ç–æ–≤–∞—Ä–∞ –≤ slov1, –µ—Å–ª–∏ –æ–Ω —Ç–∞–º –µ—Å—Ç—å, —Ç–æ
                slov1[a] += b  # ..... —Ç–æ –∫ —Ç–æ–º—É, —á—Ç–æ –∏–º–µ–µ–º + b
            else:
                slov1[a] = b  # –µ—Å–ª–∏ –µ–≥–æ –Ω–µ—Ç, —Ç–æ –ø–∏—à–µ–º –≤ —Å–ª–æ–≤–∞—Ä—å –Ω–æ–≤—ã–π –∫–ª—é—á –∏ –µ–≥–æ –∑–Ω–∞—á–µ–Ω–∏–µ


        else:
            print('–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–æ–∂–Ω–æ –±—ã—Ç—å –º–µ–Ω–µ–µ –ª–∏–±–æ —Ä–∞–≤–Ω–æ', a1)

    # –∑–∞–≤–µ—Ä—à–∞–µ–º –±–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª
    if a == 'n':
        print('_____–ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ_____', '\n', '~' * 50)
        break
    if a not in slov:
        print('!!!–Ω–µ—Ç —Ç–∞–∫–æ–≥–æ –Ω–∞–∑–≤–∞–Ω–∏—è. –ü–æ–≤—Ç–æ—Ä–∏—Ç—å –≤–≤–æ–¥!!!')
print('...... –¢–û–í–ê–† –ö–û–¢–û–†–´–ô –ë–´–õ –í–´–ë–†–ê–ù.....')
for key in slov1:
    print(key, '-', slov1[key], '—à—Ç')
print('üëâ –ò–¢–û–ì–û –ö –û–ü–õ–ê–¢–ï $', kor, 'üëà')

print('......–û–°–¢–ê–¢–û–ö –¢–û–í–ê–†–ê –ü–û–°–õ–ï –í–ê–®–ï–ì–û –®–û–ü–ò–ù–ì–ê......')
for key in slov:
    print('–Ω–∞–∏–º–µ–Ω:', key, '-', '$', slov[key]['cena'], '-', '—à—Ç:', slov[key]['kolich'])
